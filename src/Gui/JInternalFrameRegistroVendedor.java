    private void jButtonRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistroActionPerformed
        // TODO add your handling code here:
        Constantes consta = new Constantes();
        Registro registro = new Registro();
        Date fechaSistema = new Date();
        registro.setFechaRegistro(fechaSistema);
        registro.setCliente(cliente);

        registro.setListaVideos(lista);
        if (jComboBoxrEGISTRO.getSelectedIndex() == 0) {
            try {
                registro.setFechaDevolucion(fechaSistema);
                registro.setDiasPrestamo(0);
                registro.setTipo(0);
                registro.setEstado(0);
                manReg.InsertarPrestamo(registro);
                JOptionPane.showMessageDialog(null, "VENTA GUARDADO");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JInternalFrameRegistroVendedor.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JInternalFrameRegistroVendedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                registro.setDiasPrestamo(consta.getDiasPrestamo());
                registro.setFechaDevolucion(manReg.calcularFecha(fechaSistema));
                registro.setTipo(1);
                registro.setEstado(1);
                manReg.InsertarPrestamo(registro);
                JOptionPane.showMessageDialog(null, "PRESTAMO GUARDADO");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JInternalFrameRegistroVendedor.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JInternalFrameRegistroVendedor.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jButtonRegistroActionPerformed

