    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        lista = new ArrayList<>();
        membresia = new Membresia();
        cliente = manClientes.CargarPersonaTxt(Integer.parseInt(jTextFieldCedula.getText()));
        if (cliente != null) {
            membresia = manMemb.CargarMembresiaTxt(cliente.getIdMembresia());
            switch (membresia.getTipo()) {
                case "NORMAL":
                    jTextFieldmembresia.setText("NORMAL");
                    break;
                case "GOLD":
                    jTextFieldmembresia.setText("GOLD");
                    break;
                case "PLATINUM":
                    jTextFieldmembresia.setText("PLATINUM");
                    break;

            }
            if (membresia.getEstado() == 1) {
                jTextFieldEstado.setText("ACTIVA");
            } else {
                jTextFieldEstado.setText("INACTIVA");
            }
            jTextFieldNombres.setText(cliente.getNombre());
            jTextFieldDireccion.setText(cliente.getDireccion());
            jTextFieldCodigo.setText(cliente.getCodigoCliente());
            jTextFieldCorreo.setText(cliente.getCorreo());
        } else {
            JOptionPane.showMessageDialog(null, "No existe el cliente");
            jTextFieldCedula.setText("");
        }


    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jTextFieldNombresKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNombresKeyTyped
        // TODO add your handling code here:
        char tecla;
        tecla = evt.getKeyChar();
        if (!Character.isLetter(tecla) && tecla != KeyEvent.VK_SPACE && tecla != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
            getToolkit().beep();
        }
    }//GEN-LAST:event_jTextFieldNombresKeyTyped

