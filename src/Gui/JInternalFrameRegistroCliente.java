    private void jButtonBuscarpeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarpeliActionPerformed
        // TODO add your handling code here:
        video = manVideo0.CargarVideoTxt(jTextFieldBuscarTitulo.getText());
        if (video != null) {
            jTextFieldtitulo.setText(video.getTitulo());
            jTextFieldDirector.setText(video.getDirector());
            jTextFieldGENERO.setText(video.getGenero());
            jTextFieldActor.setText(video.getActor());
            jTextFieldAnio.setText(String.valueOf(video.getAnio()));
            jTextFieldRenta.setText(String.valueOf(video.getPrecioRenta()));
            jTextFieldVenta.setText(String.valueOf(video.getPrecioVenta()));
            jTextFieldCantidad.setText(String.valueOf(video.getCantidad()));
            if (jComboBoxrEGISTRO.getSelectedIndex() == 0) {
                jTextFieldPrecioTotal.setText(String.valueOf(manReg.calcularPrecio(
                        video.getPrecioVenta(), membresia.getDescuento())));
            } else {
                jTextFieldPrecioTotal.setText(String.valueOf(manReg.calcularPrecio(
                        video.getPrecioRenta(), membresia.getDescuento())));
            }

        } else {
            JOptionPane.showMessageDialog(null, "No existe el video");
            jTextFieldtitulo.setText("");
        }


    }//GEN-LAST:event_jButtonBuscarpeliActionPerformed

    private void jButtonRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistroActionPerformed
        // TODO add your handling code here:
        ArrayList<Video> list = new ArrayList<>();
        Constantes consta = new Constantes();
        Registro registro = new Registro();
        Date fechaSistema = new Date();
        registro.setFechaRegistro(fechaSistema);
        registro.setCliente(cliente);
        list.add(video);
        registro.setListaVideos(list);
        if (jComboBoxrEGISTRO.getSelectedIndex() == 0) {
            try {
                registro.setFechaDevolucion(fechaSistema);
                registro.setDiasPrestamo(0);
                registro.setTipo(0);
                registro.setEstado(0);
                manReg.InsertarPrestamo(registro);
                JOptionPane.showMessageDialog(null, "VENTA GUARDADO");

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JInternalFrameRegistroCliente.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JInternalFrameRegistroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                registro.setDiasPrestamo(consta.getDiasPrestamo());
                registro.setFechaDevolucion(manReg.calcularFecha(fechaSistema));
                registro.setTipo(1);
                registro.setEstado(1);
                manReg.InsertarPrestamo(registro);
                JOptionPane.showMessageDialog(null, "PRESTAMO GUARDADO");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JInternalFrameRegistroCliente.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JInternalFrameRegistroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        jTextFieldBuscarTitulo.setText("");
        jTextFieldtitulo.setText("");
        jTextFieldDirector.setText("");
        jTextFieldGENERO.setText("");
        jTextFieldActor.setText("");
        jTextFieldAnio.setText("");
        jTextFieldRenta.setText("");
        jTextFieldVenta.setText("");
        jTextFieldCantidad.setText("");
        jTextFieldPrecioTotal.setText("");

    }//GEN-LAST:event_jButtonRegistroActionPerformed

    private void jComboBoxrEGISTROActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxrEGISTROActionPerformed
        // TODO add your handling code here:
        if (jComboBoxrEGISTRO.getSelectedIndex() == 0) {
            jTextFieldVenta.setVisible(true);
            jLabelVenta.setVisible(true);
            jTextFieldRenta.setVisible(false);
            jLabelRenta.setVisible(false);
            jButtonRegistro.setText("VENDER");
            jTextFieldPrecioTotal.setText(String.valueOf(manReg.calcularPrecio(
                    video.getPrecioVenta(), membresia.getDescuento())));

        } else {
            jTextFieldVenta.setVisible(false);
            jLabelVenta.setVisible(false);
            jTextFieldRenta.setVisible(true);
            jLabelRenta.setVisible(true);
            jButtonRegistro.setText("RENTAR");
            jTextFieldPrecioTotal.setText(String.valueOf(manReg.calcularPrecio(
                    video.getPrecioRenta(), membresia.getDescuento())));
        }
    }//GEN-LAST:event_jComboBoxrEGISTROActionPerformed


