    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        Point point = evt.getPoint();
        int row = jTable1.rowAtPoint(point);
        if (tipo == 0) {
            jTextAreaResenia.setEditable(true);
        } else {
            jTextAreaResenia.setEditable(false);
        }
        if (evt.getClickCount() == 1) {
            for (Video ListaVideo : lstVideos) {
                if (Integer.parseInt((String) jTable1.getValueAt(row, 0)) == ListaVideo.getIdVideo()) {
                    objAux = ListaVideo;
                    jTextAreaResenia.setText((String) jTable1.getValueAt(row, 9));
                    jLabelVideoR.setVisible(true);
                    //JPanel rVideo = new JPanel();
                    //rVideo.setVisible(true);

                }
            }

        }


    }//GEN-LAST:event_jTable1MouseClicked

