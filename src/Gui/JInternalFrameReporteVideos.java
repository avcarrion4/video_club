    public void presentar() throws ClassNotFoundException {
        lstVideos = new ArrayList();
        jTable1.removeAll();
        jTable1.setModel(new DefaultTableModel());
        Object columnas[] = {"IdVideo", "Titulo", "Director", "Genero", "Actor", "Año",
            "Renta", "Venta", "Cantidad", "Reseña"};
        lstVideos = manVid.CargarMaterialesTxt();
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        jTable1.setModel(modelo);
        for (Video video : lstVideos) {
            String NewValor[] = {String.valueOf(video.getIdVideo()),
                String.valueOf(video.getTitulo()),
                String.valueOf(video.getDirector()),
                String.valueOf(video.getGenero()),
                String.valueOf(video.getActor()),
                String.valueOf(video.getAnio()),
                String.valueOf(video.getPrecioRenta()),
                String.valueOf(video.getPrecioVenta()),
                String.valueOf(video.getCantidad()),
                String.valueOf(video.getResenia())

            };
            modelo.addRow(NewValor);
        }
    }

